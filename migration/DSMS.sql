  CREATE OR REPLACE PROCEDURE "BG12CS011"."COMPUTE_PRODUCT_STATUS" 
AS
  SV_SKU VARCHAR2(8);
  SV_VARIANT VARCHAR2(45);
  SV_CURR_QTY NUMBER;
  SV_QTY_THR NUMBER;
  CURSOR PRODUCT_STATUS_CURSOR IS 
  SELECT SKU,VARIANT,STOCK_QUANTITY,QUANTITY_THRESHOLD
  FROM PRODUCT_DESCRIPTION;
BEGIN
OPEN PRODUCT_STATUS_CURSOR;
  LOOP
    FETCH PRODUCT_STATUS_CURSOR INTO SV_SKU,SV_VARIANT,SV_CURR_QTY,SV_QTY_THR;
    EXIT WHEN PRODUCT_STATUS_CURSOR%NOTFOUND;
    IF (SV_CURR_QTY=0 OR SV_CURR_QTY IS NULL) THEN
      UPDATE PRODUCT_DESCRIPTION SET STATUS='Out of Stock' WHERE SKU=SV_SKU AND (VARIANT=SV_VARIANT OR VARIANT IS NULL);
    ELSIF (SV_QTY_THR IS NOT NULL AND SV_CURR_QTY < SV_QTY_THR) THEN
      UPDATE PRODUCT_DESCRIPTION SET STATUS='Running Low' WHERE SKU=SV_SKU AND (VARIANT=SV_VARIANT OR VARIANT IS NULL);
    ELSE
      UPDATE PRODUCT_DESCRIPTION SET STATUS='Available' WHERE SKU=SV_SKU AND (VARIANT=SV_VARIANT OR VARIANT IS NULL);
    END IF;
  END LOOP;
  DBMS_OUTPUT.PUT_LINE('Updated status for ' || PRODUCT_STATUS_CURSOR%ROWCOUNT || ' products.');
CLOSE PRODUCT_STATUS_CURSOR;
END COMPUTE_PRODUCT_STATUS;

