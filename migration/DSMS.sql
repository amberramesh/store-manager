  CREATE OR REPLACE FORCE VIEW "BG12CS011"."V_NEW_ORDER" ("SKU", "PRODUCT_NAME", "VARIANT", "QUANTITY", "ORDER_ID") AS 
  SELECT OP.SKU,P.PRODUCT_NAME,OP.VARIANT,OP.QUANTITY  ||' '|| PD.QUANTITY_UNIT AS QUANTITY, OP.ORDER_ID
FROM PRODUCT P, ORDER_PARTICULAR OP, PRODUCT_DESCRIPTION PD 
WHERE P.SKU=PD.SKU AND PD.SKU=OP.SKU AND PD.VARIANT=OP.VARIANT
;
--------------------------------------------------------
--  DDL for View V_PRODUCTS
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "BG12CS011"."V_PRODUCTS" ("SKU", "PRODUCT_NAME", "VARIANT", "QUANTITY", "DEPARTMENT_NAME", "PRODUCT_TYPE") AS 
  SELECT P.SKU,P.PRODUCT_NAME,PD.VARIANT,PD.STOCK_QUANTITY || ' ' || PD.QUANTITY_UNIT AS QUANTITY,
D.DEPARTMENT_NAME,P.PRODUCT_TYPE FROM
PRODUCT P LEFT OUTER JOIN PRODUCT_DESCRIPTION PD 
ON P.SKU=PD.SKU, DEPARTMENT D WHERE P.DEPARTMENT_CODE=D.DEPARTMENT_CODE
ORDER BY P.SKU
;
--------------------------------------------------------
--  DDL for View V_PRODUCTS_WO_VARIANT
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "BG12CS011"."V_PRODUCTS_WO_VARIANT" ("SKU", "PRODUCT_NAME", "TOTAL_QUANTITY", "DEPARTMENT_NAME", "PRODUCT_TYPE") AS 
  SELECT P.SKU,P.PRODUCT_NAME,SUM(PD.STOCK_QUANTITY) AS TOTAL_QUANTITY,D.DEPARTMENT_NAME,P.PRODUCT_TYPE
FROM PRODUCT P LEFT JOIN PRODUCT_DESCRIPTION PD ON
P.SKU=PD.SKU, DEPARTMENT D
WHERE P.DEPARTMENT_CODE=D.DEPARTMENT_CODE
GROUP BY P.SKU,P.PRODUCT_NAME,D.DEPARTMENT_NAME,P.PRODUCT_TYPE
ORDER BY P.SKU
;
--------------------------------------------------------
--  DDL for View V_STOCK
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "BG12CS011"."V_STOCK" ("PRODUCT_NAME", "VARIANT", "DEPARTMENT_NAME", "QTY_AVAILABLE", "QTY_THRESHOLD", "STATUS") AS 
  SELECT P.PRODUCT_NAME,PD.VARIANT,D.DEPARTMENT_NAME,
PD.STOCK_QUANTITY ||' '|| PD.QUANTITY_UNIT AS QTY_AVAILABLE,PD.QUANTITY_THRESHOLD ||' '|| PD.QUANTITY_UNIT AS QTY_THRESHOLD,PD.STATUS
FROM PRODUCT P,DEPARTMENT D, PRODUCT_DESCRIPTION PD
WHERE P.DEPARTMENT_CODE=D.DEPARTMENT_CODE AND P.SKU=PD.SKU
ORDER BY P.SKU
;
